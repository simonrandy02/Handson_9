package Latihan3;

public class Kalkulator {
    // Overload 1
    public int tambah(int a, int b) {
        return a + b;
    }

    // Overload 2
    public int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // Overload 3
    public double tambah(double a, double b) {
        return a + b;
    }
}
